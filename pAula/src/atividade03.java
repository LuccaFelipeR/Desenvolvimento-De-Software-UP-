public class atividade03 {
        public static void main(String[] args) {
            int i = 5;
            int j = i++;
            int x = ++i;
            int y = j + x;

            System.out.println("Os valores correspondente as variaveis são; ");
            System.out.println(i + " " + j + " " + x + " " + y);

        };
};